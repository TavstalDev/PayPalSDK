namespace Tavstal.PayPalSDK.Constants.Experience;

/// <summary>
/// Provides constants for contact preferences in the PayPal SDK.
/// </summary>
public class ContactPreference
{
    /// <summary>
    /// Represents a preference where no contact information is provided.
    /// </summary>
    public const string NO_CONTACT_INFO = "NO_CONTACT_INFO";

    /// <summary>
    /// Represents a preference to update contact information.
    /// </summary>
    public const string UPDATE_CONTACT_INFO = "UPDATE_CONTACT_INFO";

    /// <summary>
    /// Represents a preference to retain existing contact information.
    /// </summary>
    public const string RETAIN_CONTACT_INFO = "RETAIN_CONTACT_INFO";
}