namespace Tavstal.PayPalSDK.Constants.Experience;

/// <summary>
/// Provides constants for landing page preferences in the PayPal SDK.
/// </summary>
public class LandingPages
{
    /// <summary>
    /// Represents a preference to display the login page.
    /// </summary>
    public const string LOGIN = "LOGIN";

    /// <summary>
    /// Represents a preference to display the guest checkout page.
    /// </summary>
    public const string GUEST_CHECKOUT = "GUEST_CHECKOUT";

    /// <summary>
    /// Represents no specific preference for the landing page.
    /// </summary>
    public const string NO_PREFERENCE = "NO_PREFERENCE";
}