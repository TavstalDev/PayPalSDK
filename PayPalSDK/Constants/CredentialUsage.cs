namespace Tavstal.PayPalSDK.Constants;

/// <summary>
/// Provides constants for credential usage in the PayPal SDK.
/// </summary>
public class CredentialUsage
{
    /// <summary>
    /// Represents the first usage of a credential.
    /// </summary>
    public const string FIRST = "FIRST";

    /// <summary>
    /// Represents subsequent usage of a credential.
    /// </summary>
    public const string SUBSEQUENT = "SUBSEQUENT";

    /// <summary>
    /// Represents derived usage of a credential.
    /// </summary>
    public const string DERIVED = "DERIVED";
}